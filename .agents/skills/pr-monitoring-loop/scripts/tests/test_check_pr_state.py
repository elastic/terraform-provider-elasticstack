HEAD_SHA = "headsha1234567890"
OLD_SHA = "oldsha000000000000"


def _pr(head_sha=HEAD_SHA, **overrides):
    base = {
        "number": 42,
        "url": "https://example.test/pulls/42",
        "state": "OPEN",
        "isDraft": False,
        "headRefName": "feature/x",
        "headRefOid": head_sha,
        "baseRefName": "main",
        "baseRefOid": "basesha",
        "reviewDecision": "REVIEW_REQUIRED",
        "mergeable": "MERGEABLE",
        "mergeStateStatus": "CLEAN",
        "labels": [],
        "statusCheckRollup": [],
        "title": "test PR",
        "updatedAt": "2026-05-07T00:00:00Z",
        "author": {"login": "human-dev"},
    }
    base.update(overrides)
    return base


def _comment(cid, login, body="hi", created="2026-05-07T01:00:00Z"):
    return {
        "id": cid,
        "user": {"login": login, "type": "Bot" if login.endswith("[bot]") else "User"},
        "body": body,
        "created_at": created,
        "updated_at": created,
    }


def _review_comment(cid, login, body="line note", created="2026-05-07T01:00:00Z"):
    return {
        "id": cid,
        "user": {"login": login, "type": "Bot" if login.endswith("[bot]") else "User"},
        "body": body,
        "path": "src/foo.go",
        "created_at": created,
        "updated_at": created,
    }


def _review(rid, login, state, commit_id=HEAD_SHA, submitted="2026-05-07T01:00:00Z", body=""):
    return {
        "id": rid,
        "user": {"login": login, "type": "Bot" if login.endswith("[bot]") else "User"},
        "state": state,
        "commit_id": commit_id,
        "submitted_at": submitted,
        "body": body,
    }


VERIFY_REPORT_BODY = (
    "## Verification Report: `some-change`\n\n"
    "Generated by OpenSpec verify (label) for issue #42"
)


def _verify_review(rid, state, commit_id=HEAD_SHA, submitted="2026-05-07T01:05:00Z"):
    """A review posted by the verify-openspec workflow (runs as github-actions[bot])."""
    return _review(
        rid,
        "github-actions[bot]",
        state,
        commit_id=commit_id,
        submitted=submitted,
        body=VERIFY_REPORT_BODY,
    )


def _label_event(action, at, label_name="verify-openspec"):
    return {
        "event": action,
        "created_at": at,
        "label": {"name": label_name},
    }


def _thread(tid, comments, resolved=False, outdated=False):
    return {
        "id": tid,
        "isResolved": resolved,
        "isOutdated": outdated,
        "path": "src/foo.go",
        "line": 1,
        "originalLine": 1,
        "comments": {"nodes": comments},
    }


def _thread_comment(node_id, db_id, login, created="2026-05-07T01:00:00Z"):
    return {
        "id": node_id,
        "databaseId": db_id,
        "author": {"login": login},
        "body": "please change",
        "createdAt": created,
        "updatedAt": created,
        "diffHunk": "@@",
        "path": "src/foo.go",
        "line": 1,
        "originalLine": 1,
        "outdated": False,
        "url": "https://example.test/c",
    }


