def test_main_emits_transient_exit_when_pr_view_returns_empty(cps, monkeypatch, capsys):
    monkeypatch.setattr(cps, "pr_view", lambda _: {})
    monkeypatch.setattr(cps, "repo_info", lambda: {"owner": "o", "name": "r"})
    rc = cps.main(["999"])
    assert rc == cps.EXIT_TRANSIENT
    out = capsys.readouterr().out
    body = json.loads(out)
    assert body.get("transient") is True


def test_main_emits_transient_exit_when_gh_repeatedly_fails(cps, monkeypatch, capsys):
    def failing(_args, *, default=None, allow_failure=False, retries=1):
        raise cps.TransientGhError(json.dumps({"error": "boom", "transient": True}))

    monkeypatch.setattr(cps, "gh_json", failing)
    rc = cps.main(["999"])
    assert rc == cps.EXIT_TRANSIENT
    out = capsys.readouterr().out
    body = json.loads(out)
    assert body.get("transient") is True


