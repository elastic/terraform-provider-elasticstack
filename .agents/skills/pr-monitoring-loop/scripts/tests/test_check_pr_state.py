def test_first_run_no_state_treats_all_comments_as_new(cps):
    kwargs = _empty_kwargs()
    kwargs["issue_comment_data"] = [
        _comment(1, "human-dev"),
        _comment(2, "macroscope[bot]"),
    ]
    kwargs["review_comment_data"] = [_review_comment(10, "verify-openspec[bot]")]
    payload, _ = cps.compute_payload(**kwargs)
    assert payload["summary"]["comments"]["newIssueComments"] == 2
    assert payload["summary"]["comments"]["newReviewComments"] == 1
    assert "issue_comments" in payload["summary"]["actionable"]
    assert "review_comments" in payload["summary"]["actionable"]


def test_seen_ids_silence_known_comments_and_bot_comments(cps):
    kwargs = _empty_kwargs()
    kwargs["issue_comment_data"] = [
        _comment(1, "human-dev"),
        _comment(2, "verify-openspec[bot]"),
    ]
    kwargs["state"] = {
        "seenIssueCommentIds": [1, 2],
        "lastPolledAt": "2026-05-07T05:00:00Z",
        "lastHeadSha": HEAD_SHA,
    }
    payload, _ = cps.compute_payload(**kwargs)
    assert payload["summary"]["comments"]["newIssueComments"] == 0
    assert "issue_comments" not in payload["summary"]["actionable"]


def test_bot_comment_actionable_first_then_silenced_after_state_persist(cps):
    bot_comment = _comment(99, "verify-openspec[bot]", body="something to address")
    kwargs = _empty_kwargs()
    kwargs["issue_comment_data"] = [bot_comment]
    payload, new_state = cps.compute_payload(**kwargs)
    assert payload["summary"]["comments"]["newIssueComments"] == 1
    assert "issue_comments" in payload["summary"]["actionable"]
    assert 99 in new_state["seenIssueCommentIds"]

    # Second run with the persisted state -> silenced.
    kwargs2 = _empty_kwargs()
    kwargs2["issue_comment_data"] = [bot_comment]
    kwargs2["state"] = new_state
    payload2, _ = cps.compute_payload(**kwargs2)
    assert payload2["summary"]["comments"]["newIssueComments"] == 0


