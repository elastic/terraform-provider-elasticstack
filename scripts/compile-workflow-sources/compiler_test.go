func TestRunCheckModeVerboseControlsDiffOutput(t *testing.T) {
	rootDir := t.TempDir()

	workflowDir := filepath.Join(rootDir, ".github", "workflows-src", "example")
	if err := os.MkdirAll(workflowDir, 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.MkdirAll(filepath.Join(rootDir, ".github", "workflows"), 0o755); err != nil {
		t.Fatal(err)
	}

	templatePath := filepath.Join(workflowDir, "workflow.md.tmpl")
	outputPath := filepath.Join(rootDir, ".github", "workflows", "example.md")

	writeFile(t, templatePath, "---\nname: Example\n---\n\nwith:\n  script: |\n    core.info('hello');\n")
	writeFile(t, outputPath, "stale\n")

	testCases := []struct {
		name        string
		args        []string
		expected    []string
		notExpected []string
	}{
		{
			name: "without verbose",
			args: []string{
				"--check",
				"--root", rootDir,
				"--template", templatePath,
				"--output", outputPath,
			},
			expected: []string{
				"generated workflow sources are out of date:",
				"- .github/workflows/example.md",
			},
			notExpected: []string{
				"--- a/.github/workflows/example.md",
			},
		},
		{
			name: "with verbose",
			args: []string{
				"--check",
				"--verbose",
				"--root", rootDir,
				"--template", templatePath,
				"--output", outputPath,
			},
			expected: []string{
				"generated workflow sources are out of date:",
				"- .github/workflows/example.md",
				"--- a/.github/workflows/example.md",
				"+++ b/.github/workflows/example.md",
			},
		},
	}

	for _, testCase := range testCases {
		t.Run(testCase.name, func(t *testing.T) {
			stderr := openTempFile(t)
			err := run(testCase.args, stderr, stderr)
			if err == nil {
				t.Fatal("expected check mode to report drift")
			}

			for _, expected := range testCase.expected {
				assertContains(t, err.Error(), expected)
			}
			for _, unexpected := range testCase.notExpected {
				assertNotContains(t, err.Error(), unexpected)
			}
		})
	}
}

func TestCompileWorkflowDetectsIncludeCycles(t *testing.T) {
	rootDir := t.TempDir()

	workflowDir := filepath.Join(rootDir, ".github", "workflows-src", "example")
	if err := os.MkdirAll(filepath.Join(workflowDir, "scripts"), 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.MkdirAll(filepath.Join(rootDir, ".github", "workflows"), 0o755); err != nil {
		t.Fatal(err)
	}

	templatePath := filepath.Join(workflowDir, "workflow.md.tmpl")
	aPath := filepath.Join(workflowDir, "scripts", "a.js")
	bPath := filepath.Join(workflowDir, "scripts", "b.js")
	outputPath := filepath.Join(rootDir, ".github", "workflows", "example.md")

	writeFile(t, aPath, "//include: b.js\n")
	writeFile(t, bPath, "//include: a.js\n")
	writeFile(t, templatePath, "with:\n  x-script-include: scripts/a.js\n")

	_, err := CompileWorkflow(CompileOptions{
		TemplatePath: templatePath,
		OutputPath:   outputPath,
		RootDir:      rootDir,
	})
	if err == nil {
		t.Fatal("expected include cycle error")
	}

	assertContains(t, err.Error(), "include cycle detected")
}

