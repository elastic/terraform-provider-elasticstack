func TestCompileWorkflowExpandsNestedIncludes(t *testing.T) {
	rootDir := t.TempDir()

	workflowDir := filepath.Join(rootDir, ".github", "workflows-src", "example")
	if err := os.MkdirAll(filepath.Join(workflowDir, "scripts"), 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.MkdirAll(filepath.Join(rootDir, ".github", "workflows"), 0o755); err != nil {
		t.Fatal(err)
	}

	templatePath := filepath.Join(workflowDir, "workflow.md.tmpl")
	scriptPath := filepath.Join(workflowDir, "scripts", "example.inline.js")
	helperPath := filepath.Join(workflowDir, "helper.js")
	outputPath := filepath.Join(rootDir, ".github", "workflows", "example.md")

	writeFile(t, helperPath, "function helper() {\n  return 'ok';\n}\n")
	writeFile(t, scriptPath, "//include: ../helper.js\n\ncore.info(helper());\n")
	writeFile(t, templatePath, "---\nname: Example\n---\n\nwith:\n  x-script-include: scripts/example.inline.js\n")

	if _, err := CompileWorkflow(CompileOptions{
		TemplatePath: templatePath,
		OutputPath:   outputPath,
		RootDir:      rootDir,
	}); err != nil {
		t.Fatal(err)
	}

	generated := readFile(t, outputPath)

	assertContains(t, generated, "# This file is generated by")
	assertNotContains(t, generated, "//include:")
	assertContains(t, generated, "with:\n  script: |\n    function helper() {")
	assertContains(t, generated, "core.info(helper());")
}

func TestCompileWorkflowCheckModeReportsDrift(t *testing.T) {
	rootDir := t.TempDir()

	workflowDir := filepath.Join(rootDir, ".github", "workflows-src", "example")
	if err := os.MkdirAll(workflowDir, 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.MkdirAll(filepath.Join(rootDir, ".github", "workflows"), 0o755); err != nil {
		t.Fatal(err)
	}

	templatePath := filepath.Join(workflowDir, "workflow.md.tmpl")
	outputPath := filepath.Join(rootDir, ".github", "workflows", "example.md")

	writeFile(t, templatePath, "---\nname: Example\n---\n\nwith:\n  script: |\n    core.info('hello');\n")
	writeFile(t, outputPath, "stale")

	result, err := CompileWorkflow(CompileOptions{
		TemplatePath: templatePath,
		OutputPath:   outputPath,
		RootDir:      rootDir,
		Check:        true,
	})
	if err != nil {
		t.Fatal(err)
	}

	if !result.Changed {
		t.Fatal("expected changed output to be reported")
	}
	if result.Diff != "" {
		t.Fatalf("expected diff to be empty without verbose output, got:\n%s", result.Diff)
	}
}

